% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
    set(handles.listbox2, 'String', '');
    set(handles.listbox4, 'String', '');
    set(handles.listbox1, 'String', '');
    set(handles.listbox3, 'String', '');
    frameLength = str2double(get(handles.edit8, 'String')); % d³ugoœæ danych u¿ytecznych w ramce
    dataSize = str2double(get(handles.edit9, 'String')); % rozmiar danych wejœciowych
    amountWithoutFrame = 100; %wielkoœæ charakteryzuj¹ca czas, po którym odbiornik zakoñczy nas³uchiwanie
    amountBadFrame = 10; %maksymalna iloœæ retransmisji uszkodzonej ramki
    data = round(rand(1,dataSize)); %dane wejœciowe
    dataTOstring = num2str(data);
    set(handles.listbox1, 'String',dataTOstring);
    newData = [];% dane wyjœciowe
    transmissionEnd = false;
    %true - addytywny, false - multiplikatywny
    if get(handles.edit1, 'String') == '0'
        isScramblerAdditive = true;
    end
    if get(handles.edit1, 'String') == '1'
        isScramblerAdditive = false;
    end
    % 1 - bit parzystoœci (crc-1), 8 - crc-8, 0 - nic 
    crc = str2double(get(handles.edit10, 'String'));
    counterWithoutFrame = 0; %iloœæ nieodebranych ramek
    counterBadFrame = 0; %iloœæ wykrytych uszkodzonych ramek
    licz = 1;
    while transmissionEnd == false
        Enkoder;
        frameIsBad = true;
        liczstr = num2str(licz);
        old_lb2 = get(handles.listbox2, 'String');
        new_lb2 = strvcat(old_lb2, liczstr);
        set(handles.listbox2, 'String', new_lb2);
        old_lb4 = get(handles.listbox4, 'String');
        new_lb4 = strvcat(old_lb4, liczstr);
        set(handles.listbox4, 'String', new_lb4);
        licz=licz+1;
        while frameIsBad
            frame = sendFrame(dataToFrame, isScramblerAdditive, crc);
            frameTOstring = num2str(frame);
            old_lb2 = get(handles.listbox2, 'String');
            new_lb2 = strvcat(old_lb2, frameTOstring);
            set(handles.listbox2, 'String', new_lb2);
            %zaklocenia
            if get(handles.edit2, 'String') == '1'
                frame=zak_bsc(frame);
            end
            if get(handles.edit2, 'String') == '2'
                frame=zak_cykl(frame);
            end
            if get(handles.edit2, 'String') == '3'
                frame=zak_los_skok(frame);
            end
            frameTOstring = num2str(frame);
            old_lb4 = get(handles.listbox4, 'String');
            new_lb4 = strvcat(old_lb4, frameTOstring);
            set(handles.listbox4, 'String', new_lb4);
            Dekoder;
            if counterWithoutFrame >= amountWithoutFrame
                frameIsBad = false;
            end
        end
    end
    newDataTOstring = num2str(newData);
    set(handles.listbox3, 'String',newDataTOstring);


